            <p>
                <span class="highlight">KAVACH</span> is a state-of-the-art electronic system which was designed to help the Indian Railways achieve Zero Accidents.1 It is an Automatic Train Protection (ATP) system which has been indigenously developed by Research Design and Standards Organisation (RDSO) in collaboration with Indian industry with trials facilitated by South Central Railway to achieve the corporate objective of safety in train operations across Indian Railways. It activates the train braking system
            </p>
            <p>
               automatically if the driver fails to control the train as per the speed restrictions. In addition, it prevents collision between two Locomotives equipped with a functional KAVACH system.
            </p>
            <img src="automatic.jpg" alt="Train Image">
            <p>
               It is a Safety Integrity Level 4 (SIL-4) certified technology with the probability of error being one in 10,000 years. 2 Once

implemented, KAVACH will be the world's cheapest automatic train collision protection system, costing 50 lakh rupees per kilometre to operate compared to about two crore rupees worldwide. It also opens avenues of export of this indigenous technology for Railways.3
            </p>
 <h2>Successful Trial of KAVACH</h2>
 <img src="downloads.jpg" alt="Train Image">
 
 <p>On 4 March 2022, the successful trial of KAVACH was conducted between Gullaguda ‚Äì Chitgidda Railway stations of South Central Railway. Shri Ashwini Vaishnaw, Union Minister of Railways, Communication and Electronics & Information Technology inspected the trial, during which a Head-on-collision situation was created with two locomotives

moving towards each other. The KAVACH system initiated the automatic braking system and halted the locomotives 380 metres apart. 5Crossing of the red signal was also tested wherein the locomotive did not cross the red signal as KAVACH necessitated the application of brakes automatically. Automatic whistle sound was loud and clear when gate signal approached. Further, KAVACH automatically reduced the speed to 30 Kmph from 60 Kmph as the locomotive entered the loop line.</p>
